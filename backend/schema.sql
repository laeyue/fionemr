-- Copy and run this script in your Supabase SQL Editor.

-- Drop existing tables if they exist (clean setup)
DROP TABLE IF EXISTS visit_logs CASCADE;
DROP TABLE IF EXISTS medication_orders CASCADE;
DROP TABLE IF EXISTS soap_notes CASCADE;
DROP TABLE IF EXISTS vitals CASCADE;
DROP TABLE IF EXISTS immunizations CASCADE;
DROP TABLE IF EXISTS patients CASCADE;
DROP TABLE IF EXISTS accounts CASCADE;


-- 1. Patients Table
CREATE TABLE patients (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    section TEXT,
    age INT,
    gender TEXT,
    status TEXT DEFAULT 'Active',
    status_color TEXT DEFAULT 'green',
    date_of_birth DATE,
    grade_level TEXT,
    allergies TEXT DEFAULT 'None',
    chronic_conditions TEXT DEFAULT 'None',
    emergency_contact_name TEXT,
    emergency_contact_phone TEXT,
    emergency_contact_relationship TEXT,
    created_at TIMESTAMPTZ DEFAULT now()
);

-- Adjust start point for IDs to simulate existing records
ALTER TABLE patients ALTER COLUMN id RESTART WITH 1000;

-- 2. Vitals Table
CREATE TABLE vitals (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    patient_id BIGINT REFERENCES patients(id) ON DELETE CASCADE,
    temperature NUMERIC(4, 1),
    heart_rate INT,
    blood_pressure TEXT,
    o2_sat INT,
    respiratory_rate INT,
    recorded_at TIMESTAMPTZ DEFAULT now()
);

-- 3. SOAP Notes Table
CREATE TABLE soap_notes (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    patient_id BIGINT REFERENCES patients(id) ON DELETE CASCADE,
    subjective TEXT,
    objective TEXT,
    assessment TEXT,
    plan TEXT,
    disposition TEXT,
    created_at TIMESTAMPTZ DEFAULT now()
);

-- 4. Medication Orders Table
CREATE TABLE medication_orders (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    patient_id BIGINT REFERENCES patients(id) ON DELETE CASCADE,
    medication TEXT NOT NULL,
    dosage TEXT NOT NULL,
    route TEXT NOT NULL,
    consent BOOLEAN DEFAULT FALSE,
    created_at TIMESTAMPTZ DEFAULT now()
);

-- 5. Visit Logs (Activity Feed / Audit Log)
CREATE TABLE visit_logs (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    patient_id BIGINT REFERENCES patients(id) ON DELETE CASCADE,
    event_type TEXT NOT NULL, -- 'Check-in', 'Vitals Recorded', 'Clinical Note Added', 'Medication Ordered'
    details TEXT,
    created_at TIMESTAMPTZ DEFAULT now()
);

